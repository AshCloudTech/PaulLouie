    <script>
        function showToast() {
            const toast = document.getElementById("toast");
            const timerBar = document.getElementById("toast-timer");

            timerBar.style.transition = "none";
            timerBar.style.width = "100%";

            setTimeout(() => {
                timerBar.style.transition = "width 3s linear";
                timerBar.style.width = "0%";
            }, 50);

            toast.style.opacity = "1";
            toast.style.transform = "translateX(0)";

            setTimeout(() => {
                toast.style.opacity = "0";
                toast.style.transform = "translateX(120%)";
            }, 3000);
        }

        let isSending = false;

        document.getElementById("contact-form").addEventListener("submit", function(e) {
            e.preventDefault();

            if (isSending) return;
            isSending = true;

            const form = this;
            const btn = form.querySelector("button[data-loading-text]");
            const originalText = btn.innerHTML;

            btn.innerHTML = btn.getAttribute("data-loading-text");
            btn.disabled = true;


            fetch("inc/sendmail.php", {
                    method: "POST",
                    body: new FormData(form)
                })
                .then(res => res.text())
                .then(() => {
                    showToast();
                    form.reset();
                    btn.innerHTML = originalText;
                    btn.disabled = false;
                    isSending = false;
                })
                .catch(() => {
                    alert("Error sending form!");
                    btn.innerHTML = originalText;
                    btn.disabled = false;
                    isSending = false;
                });
        });
    </script>





